String uelnCountryCodeAlpha3FromNum(int n) {
  final result = _uelnAlpha3[n];
  if (result == null)
    throw FormatException('Error: country code $n is invalid');
  return result;
}

String uelnCounryCodeAlpha2FromNum(int n) {
  final result = _uelnAlpha2[n];
  if (result == null)
    throw FormatException('Error: country code $n is invalid');
  return result;
}

int uelnCountryCodeNumFromAlpha2(String s) {
  if (s.length < 2) throw FormatException('Error: $s is too short');
  s = s.substring(0, 2);

  return mapReverseLookup(_uelnAlpha2, s);
}

K mapReverseLookup<K, V>(Map<K, V> map, V value) =>
    map.keys.firstWhere((k) => map[k] == value, orElse: () => null);

